class player(models.Model) :
    class SkinChoices(models.IntegerChoices):
        DEFAULT = 0, 'default'
        JESSIE = 1, 'jessie'
        JAMES = 2, 'james'

    class MapChoices(models.IntegerChoices):
        DEFAULT = 0, 'default'
        BATIMENT_1 = 1, 'batiment_1'
        BATIMENT_2 = 2, 'batiment_2'
        BATIMENT_3 = 3, 'batiment_3'
        BATIMENT_4 = 4, 'batiment_4'

    class StatusChoices(models.IntegerChoices):
        DEFAULT = 0, 'default'
        FIGHT = 1, 'fight'
        TALK = 2, 'talk'

    userID = models.IntegerField(primary_key=True , unique=True)
    posX = models.IntegerField(default=14)
    posY = models.IntegerField(default=7)
    orientation = models.CharField(default='S')
    active = models.BooleanField(default=True)
    player_skin = models.IntegerField(choices=SkinChoices.choices, default=SkinChoices.DEFAULT)
    player_map = models.IntegerField(choices=MapChoices.choices, default=MapChoices.DEFAULT)
    player_status = models.IntegerField(choices=StatusChoices.choices, default=StatusChoices.DEFAULT)

